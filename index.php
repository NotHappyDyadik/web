              4-я Промышленная Революция. Новые вызовыТехнологии меняются
              быстро как никогда, создавая новые взаимоотношения в распределении 
              межотраслевых и межгосударственных связей, в структуре трудовых ре
              сурсов и в образовании.Первая промышленная революция, обеспечишая переход от ручного труда к машинному, растянулась на многие десятилетия. Принято связывать ее с изобретением парового двигателя в XVII в., но процесс перехода от мануфактур к фабрикам продолжался в развитых странах в течение XVIII–XIX вв. Промышленная революция сильно повлияла не только на развитие науки и техники, но и на изменение структуры общества, урбанизацию, появление новых специальностей.
              Вторая промышленная революция была связана с электрификацией и организацией конвейерного производства в ХХ в. сначала автомобилей, а потом и большинства других товаров. Резко повысилась производительность труда, изменились подходы к управлению предприятиями.
              <br>Третья промышленная революция начлась в конце прошлого-начале XXI века. Она базируется на отказе от использования полезных ископаемых, переходе к возобновляемым источникам энергии в сочетании с внедрением компьютеров в производство, автоматизацией и переходом к цифровому аддитивному производству.
              «Индустрия 3.0» базируется на трех принципах.
              1) Смещение центра прибыли от этапов производства к R&D центрам - разработке и конструированию. Классическим примером стало неравномерное формирование добавленной стоимости в цепочках проект (дизайн) – создание и маркетинг – сборка.
              2) Рост производительности труда и, как следствие, сокращение синих воротничков, рабочих, непосредственно занятых в производстве.
              3) Замещение ставших за последние столетия традиционными централизованных моделей бизнеса распределенными структурами, горизонтальным взаимодействием.<br>
              Четвертая промышленная революция. 
              Несмотря на то что реалии третьей революции еще далеко не распространились по миру, назрела новая революционная ситуация – немецкая концепция «Индустрия 4.0» ( Этот термин стал официальным в 2016 году, после международного экономического форума в Давосе). В зависимость от ее реализации был поставлен сам факт существования в будущем промышленности Германии и США на фоне глобального переноса производств в Азию, Африку и другие развивающиеся страны.
              Ключевой двигатель «Индустрии 4.0» – усиленная интеграция «киберфизических систем» в заводские процессы. Производственные мощности будут взаимодействовать с производимыми товарами и адаптироваться при необходимости под новые потребности потребителей. При этом целые этапы производства будут происходить без участия человека. 
              В «Индустрии 4.0» в одну сеть через интернет объединяются не только предметы, но и станки, сборочные линии и целые заводы. Уже сейчас на некоторых предприятиях на заготовках установлены RFID-метки, которые передают необходимую информацию сборочному роботу. Отслеживаются запасы сырья, и если раньше в бизнес-школах преподавали промышленную технологию Just-in-Time как самую передовую, то в скором будущем потребность в этом подходе и соответствующих специалистах просто исчезнет. При этом все более привычной становится кастомизация, каждое изделие может быть сделано на крупной фабрике под индивидуального заказчика без повышения розничной стоимости.
              По прогнозам ведущих экономистов, в ближайшем будущем благодаря набирающему ход внедрению 3D-принтинга появится бесчисленное количество предпринимателей, работающих в мелких цехах, на дому и в гаражах, они смогут изготавливать вещи, которые прежде никогда не смогли бы изготовить. Эту тенденцию можно заметить по международным бизнес-инкубаторам и акселераторам. Если еще недавно они были рассчитаны прежде всего на интернет-бизнесы, то сейчас появляется все больше центров, ориентированных на «железо», с различными станками и 3D-принтерами в своих помещениях. Даже знаменитый инкубатор Y Combinator, где выросли многие известные интернет-гиганты, в последнее время ориентируется на реальные бизнесы. Ведь уже сейчас индивидуальные предприниматели могут создавать изделия, которые недавно были по силам лишь крупным предприятиям. У одиночек появилась возможность не только использовать компьютерные программы и обмениваться своими разработками, но и получить доступ к все еще дорогому, но стремительно дешевеющему оборудованию, предоставляющему большие возможности немедленного внедрения.
              Новая революция также может помочь восстановлению окружающей природной среды за счет лучшего управления активами, потенциально даже восстанавливая последствия тех огромные ущербов для Земли, которые причинили предыдущие промышленные революции .
              Но все эти изменения несут серьезные потенциальные риски. Шваб излагает свои опасения, что многие сообщества могут быть не в состоянии или не желают адаптироваться к этим новым технологиям, и что правительства могли не использовать или регулировать эти технологии должным образом. В книге он постулирует, что переход власти создаст важные новые проблемы безопасности, и что неравенство может расти, а не сокращаться, если государственное управление не организовано должным образом.
